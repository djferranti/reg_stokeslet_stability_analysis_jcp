    exp(1).^((-1).*c.*(epsilon.^2+z.^2).^(1/2)).* ...
    pi.^(-1).*(epsilon.^2+z.^2).^(-7/2).*(c.^4.*epsilon.^8.*( ...
    epsilon.^2+z.^2).^(3/2)+12.*(epsilon.^2+z.^2).^(7/2)+2.*c.^3.* ...
    epsilon.^6.*(epsilon.^4+(-1).*epsilon.^2.*z.^2+(-2).*z.^4)+3.* ...
    c.^2.*epsilon.^4.*(epsilon.^2+z.^2).^(1/2).*(2.*epsilon.^4+(-2).* ...
    epsilon.^2.*z.^2+z.^4)+3.*c.*(4.*epsilon.^8+4.*epsilon.^6.*z.^2+ ...
    7.*epsilon.^4.*z.^4+2.*epsilon.^2.*z.^6));
end