function dBdz=dBhat132dz(c,kx,ky,z,epsilon,x0,y0) 
dBdz = exp(-sqrt(-1).*(kx .* x0  + ky .* y0)).* (-1/48).*c.^(-1).* ...
    exp(1).^((-1).*c.*(epsilon.^2+z.^2).^(1/2)).* ...
    pi.^(-1).*z.*(epsilon.^2+z.^2).^(-5/2).*(3.*c.^2.*epsilon.^6.*( ...
    epsilon.^2+z.^2).^(1/2)+c.^3.*epsilon.^6.*(epsilon.^2+z.^2)+6.*( ...
    epsilon.^2+z.^2).^(5/2)+3.*c.*epsilon.^2.*(2.*epsilon.^4+2.* ...
    epsilon.^2.*z.^2+z.^4));
end